package ug.ite2200.chatapp.entities;

import java.util.UUID;

/**
 * @author amritaramnauth
 */
public class Room {

    private UUID id;
    private String name;

    public Room(String name) {
        this.name = name;
    }

    public UUID getId() {
        return id;
    }

    public void setId(UUID id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Room{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
